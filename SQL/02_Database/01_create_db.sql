/*
CREATE DATABASE
La declaración CREATE DATABASE se utiliza para crear una nueva base de datos SQL.

Sintaxis*/
    CREATE DATABASE databasename;/*
    

Ejemplo:  La siguiente declaración SQL crea una base de datos llamada "testDB":*/
    CREATE DATABASE testDB;/*

Consejo: asegúrese de tener privilegios de administrador antes de crear cualquier base de datos. 
Una vez creada una base de datos, puede consultarla en la lista de bases de datos con el siguiente 
comando SQL: SHOW DATABASES;