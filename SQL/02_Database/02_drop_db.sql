/*
DROP DATABASE
    La declaración DROP DATABASE se utiliza para eliminar una base de datos SQL existente.

Sintaxis*/
    DROP DATABASE databasename;/*

    Nota: tenga cuidado antes de eliminar una base de datos. ¡Eliminar una base de datos resultará en 
    la pérdida de la información completa almacenada en la base de datos!

    Ejemplo: La siguiente declaración SQL elimina la base de datos existente "testDB":*/
    DROP DATABASE testDB;/*

Consejo: asegúrese de tener privilegios de administrador antes de eliminar cualquier base de datos. 
Una vez que se elimina una base de datos, puede verificarla en la lista de bases de datos con el 
siguiente comando SQL SHOW DATABASES:;