HOSTING
    Si desea que su sitio web pueda almacenar y recuperar datos de una base de datos, su servidor web debe tener acceso a un sistema 
    de base de datos que utilice el lenguaje SQL.
    Si su servidor web está alojado en un proveedor de servicios de Internet (ISP), deberá buscar planes de alojamiento SQL.
    Las bases de datos de alojamiento SQL más comunes son MS SQL Server, Oracle, MySQL y MS Access.

Servidor MS SQL
    SQL Server de Microsoft es un software de base de datos popular para sitios web basados en bases de datos con mucho tráfico.
    SQL Server es un sistema de base de datos SQL muy potente, robusto y con todas las funciones.

Oráculo
    Oracle también es un software de base de datos popular para sitios web basados en bases de datos con mucho tráfico.
    Oracle es un sistema de base de datos SQL muy potente, robusto y con todas las funciones.

MySQL
    MySQL también es un software de base de datos popular para sitios web.
    MySQL es un sistema de base de datos SQL muy potente, robusto y con todas las funciones.
    MySQL es una alternativa económica a las costosas soluciones de Microsoft y Oracle.

Acceso MS
    Cuando un sitio web requiere sólo una base de datos simple, Microsoft Access puede ser una solución.
    MS Access no es muy adecuado para un tráfico muy elevado y no es tan potente como MySQL, SQL Server u Oracle.